package Loops;

public class example1_forLoop3_printEvenNum_From_2_to_10 
{
	public static void main(String[] args)
	{
		//print even num from 2 to 10
		//star num: 2
		//end num: 10
		//incr by 2
		
		//    2       4<=10    2+2=4
		for(int i=2; i<=10; i=i+2) 
		{
			System.out.println(i);   //2 4
		}
			
	}

}
