package Loops;

public class example1_forLoop4_printOddNum_From_1_to_9 
{
	public static void main(String[] args)
	{
		//print odd num from 1 to 9
		//star num: 1
		//end num: 9
		//incr by 2
		
		//        1  11<=9     9+2=11
		for(int i=1; i<=9; i=i+2) 
		{
			System.out.println(i);  //1 3 5 7 9
		}
			
	}

}

