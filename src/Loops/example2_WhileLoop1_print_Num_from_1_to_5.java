package Loops;

public class example2_WhileLoop1_print_Num_from_1_to_5 
{
	public static void main(String[] args) 
	{		
		//start num: 1
		//end num: 5
		//incr by 1
			
		int i=1;     //start num
		
		//   6<=5
		while(i<=5)       //end num
		{
			System.out.println(i);       //1 2 3 4 5
			i++;              // 6     //incr or decr
		}
		
	}

}
