package Loops;

public class example2_WhileLoop2_print_Num_from_21_to_50
{
	public static void main(String[] args) 
	{		
		//start num: 21
		//end num: 50
		//incr by 1
			
		int i=21;     //start num
		
		//   
		while(i<=50)       //end num
		{
			System.out.println(i);       //1 2 3 4 5
			i++;              // 6     //incr or decr
		}
		
	}

}

