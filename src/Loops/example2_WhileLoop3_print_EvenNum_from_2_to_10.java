package Loops;

public class example2_WhileLoop3_print_EvenNum_from_2_to_10 
{
	public static void main(String[] args) 
	{		
		//start num: 2
		//end num: 10
		//incr by 2
			
		int i=2;     
		
		//   12<=10
		while(i<=10)      
		{
			System.out.println(i);      //2 4 6 8 10
			i=i+2;                 //12
		}
		
	}

}

