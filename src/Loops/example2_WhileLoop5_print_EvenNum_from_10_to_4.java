package Loops;

public class example2_WhileLoop5_print_EvenNum_from_10_to_4 
{
	public static void main(String[] args) 
	{		
		//start num: 10
		//end num: 4
		//decr by 2
			
		int i=10;     
		
		//    2>=4
		while(i>=4)      
		{
			System.out.println(i);      // 10 8 6 4
			i=i-2;                 //2
		}
		
	}

}

