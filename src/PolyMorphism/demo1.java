package PolyMorphism;

public class demo1
{
	//method overloading
	
	//method with 2 int parameter
	public void addition(int a, int b)   //10, 20
	{
		System.out.println(a+b);    //10+20   = 30
	}
	
	//method with 3 int parameter
	public void addition(int a, int b, int c)
	{
		System.out.println(a+b+c);
	}

}

